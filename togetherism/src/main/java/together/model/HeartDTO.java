package together.model;


public class HeartDTO {
	
	private int heart_num;
	private int club_num;
	private String member_email;
	
	public int getHeart_num() {
		return heart_num;
	}
	public void setHeart_num(int heart_num) {
		this.heart_num = heart_num;
	}
	public int getClub_num() {
		return club_num;
	}
	public void setClub_num(int club_num) {
		this.club_num = club_num;
	}
	public String getMember_email() {
		return member_email;
	}
	public void setMember_email(String member_email) {
		this.member_email = member_email;
	}

}